    private void botaoabrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoabrirActionPerformed
        // TODO add your handling code here:
        String linha = "";

        int res = fc.showOpenDialog(null);
        File arquivo = fc.getSelectedFile();
        FileReader filereader = null;

        lista.removeAllElements();
        jList1.removeAll();
        jList1.setModel(lista);
        
        
            
        try {
            filereader = new FileReader(arquivo);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MaquinaVirtualUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        BufferedReader buff;
        buff = new BufferedReader(filereader);

        try {

            while ((linha = buff.readLine()) != null) {
                lista.addElement(linha);
                String[] palavras = {"", "", "", ""};

                int espaco = 0;
                int flag = -1;

                for (char caractere : linha.toCharArray()) // percorre todos os caracteres de uma linha e armazena na variavel caractere
                {

                    if (Character.isSpaceChar(caractere)) // se o caractere for um espaco
                    {
                        if (espaco == 0) // se primeiro caractere ou anterior nao foi um espaco, entao
                        {
                            espaco = 1; // atualiza variavel espaco
                            flag++; // incrementa flag
                        }
                        continue;
                    } else // se caractere nao for espaco
                    {
                        espaco = 0;
                    }

                    if (flag == -1) // se for o primeiro caractere da linha
                    {
                        flag++;  // se achar caractere em primeira posicao da linha, incrementa flag
                    }
                    palavras[flag] = palavras[flag] + caractere; // concatena os caracteres ate formar uma palavra
                    
                    if (palavras[1].equals("NULL")) // se for caractere NULL
                    {
                        hash.put(palavras[0], qtd_linhas); // adiciona na hashtable -> palavra[0] contem o label e k contem o numero da linha
                        palavras[0] = "NULL";
                        palavras[1] = "";
                        palavras[2] = "";
                        palavras[3] = "";
                    }
                }
                Instrucoes instrucao = new Instrucoes(palavras[0], palavras[1], palavras[2], palavras[3]);
                instrucoes.add(instrucao);

                tabelaCodigoMaquina[qtd_linhas][0] = String.valueOf(qtd_linhas);
                tabelaCodigoMaquina[qtd_linhas][1] = palavras[0];
                tabelaCodigoMaquina[qtd_linhas][2] = palavras[1];       
                tabelaCodigoMaquina[qtd_linhas][3] = palavras[2];            
                qtd_linhas++;
            }
            int i =0;
            for (Instrucoes cod : instrucoes) {
                // percorre todas as instrucoes e para as instrucoes que tem LABEL como JMP, JMPF e CALL
                if ((cod.getOperacao().equals("JMP")) || (cod.getOperacao().equals("JMPF")) || (cod.getOperacao().equals("CALL"))) {
                    String numero_linha = String.valueOf(hash.get(cod.getParam1())); // armazena o numero_linha no valor que estava no label
                    cod.setParam1(numero_linha); // atualiza o Parametro 1 para o respectivo numero da linha
                    tabelaCodigoMaquina[i][2] = numero_linha;
                }
               i++;
            }

        
        } catch (IOException ex) {
            Logger.getLogger(MaquinaVirtualUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            filereader.close();
        } catch (IOException ex) {
            Logger.getLogger(MaquinaVirtualUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botaoabrirActionPerformed

