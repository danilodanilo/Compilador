    private void adicionar_tabela() {
        
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

            int nLinha = modelo.getRowCount();
            int i;

            for (i = 0; i < nLinha; i++) 
            {
                modelo.removeRow(0);
            }
            
            for (i = 0; i <= ex.getS(); i++) 
            {
                String m = (ex.getM()[i] + 0) + "";
                modelo.addRow(new String[]{"", ""});
                jTable1.setValueAt(i, i, 0);
                jTable1.setValueAt(m, i, 1);
            }
            
            this.validate();
    }
